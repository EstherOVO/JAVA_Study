package inheritance.final_keyword;

// 클래스에 선언된 final의 경우 더 이상 상속받을 수 없음을 나타낸다.
// final public class A {

public class A {

//  메소드에 선언된 final은 더 이상 재정의(Override) 될 수 없음을 나타낸다.
// final public void method() {
//    System.out.println("메소드");

    public void method() {
        System.out.println("메소드");
    }
}
