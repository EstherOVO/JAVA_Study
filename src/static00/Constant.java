package static00;

public class Constant {

//  상수 : 변하지 않고 항상 일정한 값을 갖는 수
//  Java에서는 static final 키워드를 사용해서 클래스 레벨의 상수를 선언
//  대문자로 표기하는 것이 일반적이고 밑줄로(_) 단어를 구분한다.


//  수학 상수
    public static final double PI = 3.14159;
    public static final double E = 2.71828;


//  변하지 않는 시간 관련 상수
    public static final double HOURS_A_DAY = 24;
    public static final double MINUTES_A_HOUR = 60;
    public static final double SECONDS_A_MINUTES = 60;

//  프로그램에서 설정하는 상수
    public static final double MAX_USERS = 10000;
}
