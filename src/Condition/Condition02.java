package Condition;

public class Condition02 {
    public static void main(String[] args) {

        int age = 5;

        if (age >= 18) {
            System.out.println("성인입니다.");   // 조건이 true인 경우 수행
        }

        else {
            System.out.println("미성년자입니다."); // 조건이 false인 경우 수행
        }

    }
}
