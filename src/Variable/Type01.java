package Variable;

public class Type01 {
    public static void main(String[] args) {

        int a = 100;            // Integer 정수
        double b = 100.0;       // 실수
        boolean c = true;       // 참 거짓 논리값
        char d = 'A';           // Character 문자 하나 - 홀따옴표('') 사용
        String e = "안녕하세요";  // 문자열 - 쌍따옴표("") 사용

    }
}
